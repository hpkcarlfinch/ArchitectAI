const DEFAULT_UNITS: BlueprintSchema["metadata"]["units"] = "m";
const DEFAULT_RENDER_MODE: RenderMode = "2d";

const ALLOWED_DOOR_SWINGS: DoorSwing[] = ["left", "right", "double", "sliding", "none"];
const ALLOWED_DOOR_TYPES: DoorType[] = ["single", "double", "sliding", "pocket", "entry"];
const ALLOWED_WINDOW_TYPES: WindowType[] = ["fixed", "casement", "sliding", "picture", "awning"];
const ALLOWED_LINE_STYLES: LineStyle[] = ["solid", "dashed", "dotted"];
const ALLOWED_MATERIAL_CATEGORIES: MaterialCategory[] = ["wall", "floor", "roof", "counter", "fixture", "generic"];
const ALLOWED_ANNOTATION_KINDS: AnnotationKind[] = [
  "label",
  "dimension",
  "materialTag",
  "elevationMarker",
  "sectionMarker",
  "note",
];
