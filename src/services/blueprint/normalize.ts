export const normalizeBlueprint = (value: unknown): BlueprintSchema => {
  const source = asRecord(value);
  const floors = Array.isArray(source.floors) ? source.floors.map(normalizeFloor) : [];

  const normalized: BlueprintSchema = {
    metadata: normalizeMetadata(source.metadata),
    floors: floors.length > 0 ? floors : createDefaultBlueprint().floors,
    materials: Array.isArray(source.materials) ? source.materials.map(normalizeMaterial) : [],
    annotations: Array.isArray(source.annotations) ? source.annotations.map(normalizeAnnotation) : [],
    roofZones: Array.isArray(source.roofZones) ? source.roofZones.map(normalizeRoofZone) : [],
    legends: Array.isArray(source.legends) ? source.legends.map((legendValue) => normalizeLegend(legendValue)) : [],
    roofHints: {
      type: asString(asRecord(source.roofHints).type) || undefined,
      pitch: asString(asRecord(source.roofHints).pitch) || undefined,
    },
    materialHints: {
      wall: asString(asRecord(source.materialHints).wall) || undefined,
      roof: asString(asRecord(source.materialHints).roof) || undefined,
      floor: asString(asRecord(source.materialHints).floor) || undefined,
    },
  };

  normalized.floors = normalized.floors.map((floor, floorIndex) => ({
    ...floor,
    fixtures: floor.fixtures ?? [],
    annotations: floor.annotations ?? [],
    rooms: floor.rooms.length > 0 ? floor.rooms : createDefaultBlueprint().floors[0].rooms,
    walls: floor.walls.length > 0 ? floor.walls : createDefaultBlueprint().floors[0].walls,
    id: floor.id || "floor_" + floorIndex,
  }));

  return normalized;
};

export const normalizeProjectBlueprintValue = (value: unknown): BlueprintSchema | null => {
  if (value === null || value === undefined) {
    return null;
  }
  return normalizeBlueprint(value);
};

export const ensureLegendDefinitions = (blueprint: BlueprintSchema): BlueprintSchema => {
  const legends = blueprint.legends ?? [];
  if (legends.length > 0) {
    return blueprint;
  }

  const materialLegend: LegendDefinition = {
    title: "Materials",
    items: (blueprint.materials ?? []).map((material, index) => ({
      id: "legend_material_" + index,
      category: "material",
      key: material.id,
      label: material.label,
      sample: material.hatchPattern,
    })),
  };

  return {
    ...blueprint,
    legends: [materialLegend],
  };
};

